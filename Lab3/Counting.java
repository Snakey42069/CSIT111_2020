public class Counting {

	public static void main(String[] args) {
		int N = 1;
		
		for (int i =3; i>0; i--){
			System.out.print(i + " ");
		}
        System.out.println("Go!");
        
        while (N < 4) {
            System.out.print(N + " ");
            N = N + 1;
        }

        System.out.println("Stop!");
	}

}